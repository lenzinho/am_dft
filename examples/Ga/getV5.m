function [a] = getV5(v)
a(1,1) = v(1);
a(1,2) = v(2);
a(2,2) = v(3);
a(1,3) = v(4);
a(2,3) = v(5);
a(3,3) = v(6);
a(1,4) = v(7);
a(2,4) = v(8);
a(3,4) = v(9);
a(4,4) = v(10);
a(2,1) = -1.00000*a(1,2);
a(3,1) = -1.00000*a(1,3);
a(4,1) = -1.00000*a(1,4);
a(3,2) = +1.00000*a(2,3);
a(4,2) = +1.00000*a(2,4);
a(4,3) = +1.00000*a(3,4);
end
