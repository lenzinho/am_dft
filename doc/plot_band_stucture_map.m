%% functions

% orientations
function [nx,ny,R] = orientations(i)
    % define reorientation matrix to rotation to 011
    Rx_ = @(t) [1 0 0; 0 cosd(t) -sind(t); 0 sind(t) cosd(t)];
    Ry_ = @(t) [cosd(t) 0 sind(t); 0 1 0; -sind(t) 0 cosd(t)];
    Rz_ = @(t) [cosd(t) -sind(t) 0; sind(t) cosd(t) 0; 0 0 1];
    switch i
        case 1
            % Gamma - X; Gamma - L
            nx = 1;
            ny = sqrt(2);
            R = Rx_(-45);
        case 2
            % Gamma - X; Gamma - L
            nx = 2;
            ny = sqrt(2);
            bas = (ones(3)-eye(3));
            R = inv(bas) * Rx_(-45);
    end
end

% plot reciporcal lattice points
function plot_G(k,bas,recbas,reorient)

    % generate, shift, and plot points
    Q = [-1:1]; [Gx,Gy,Gz]=meshgrid(Q,Q,Q);
    G = [Gx(:),Gy(:),Gz(:)]*recbas*reorient + get_nearest_G(k,bas,recbas);
    plot3(G(:,1),G(:,2),G(:,3),'.k','markersize',20);

    function G = get_nearest_G(k,bas,recbas)
    % k in [1/nm], G in [1/nm]
        k_frac = k*bas;
        k_fbz = mod(k_frac,1);
        G = (k_frac-k_fbz)*recbas;
    end
end

