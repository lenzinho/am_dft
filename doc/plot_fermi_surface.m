function [bool] = is_in_fbz(K,iM)

    [x,y,z] = meshgrid([-1:1],[-1:1],[-1:1]);
    
    % reciprocal lattice points
    grid_points = iM * [ x(:), y(:), z(:) ].';

    bool = true;
    for i = 1:27
        G = grid_points(:,i);
        if 2*dot(K,G) > dot(G,G)
            bool = false; return; 
        end
    end
end

