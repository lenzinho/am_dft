clear;clc;

% load rotation matrices in tight binding representation
sym = load_symmetries();

% set tight binding matrix elements
vsk = [1.158,-0.476,-4.085,1.045,1.038,-0.130,-0.220,-0.074,-0.533,0.055,0.112,-0.091,0.432,0.317];

% compute k points (cart)
N = 31; Q = ([0:(N-1)]/(N-1)-1/2)*2; [x,y,z] = meshgrid(Q,Q,Q);

% centering matrix 
M = (ones(3)-eye(3))/2; iM = inv(M);

% get kpoints
nks = numel(x); k(1:nks,1:3) = [x(:),y(:),z(:)];

% define bands
nbands = 8;

% get eigenvalues and spectral function
B = zeros(nbands,nks);
for ik = 1:nks
%     if is_in_fbz(k(ik,1:3),iM)
    if k(ik,2)>0
        B(1:nbands,ik) = sort(real(eig( single(get_H_numeric_frac(sym, vsk, k(ik,1:3)*M, [1:100])) )));
    else
        B(1:nbands,ik) = NaN;
    end
end

% define Fermi energy
Ef = 0;

% reshape array into tensor
reshape_ = @(M) reshape(M,[N,N,N]);
v = zeros(N,N,N,nbands); for i = 1:nbands; v(:,:,:,i) = reshape_(B(i,:)); end

% plot fermi surface
figure(1); set(gcf,'color','w'); hold on;
for i = 1:nbands; isosurface(x,y,z,v(:,:,:,i),Ef); end
box on; daspect([1 1 1]); grid on; axis tight; axis off;

% make fermi surface patch transparent
h = findobj(gca,'type','patch'); set(h,'facealpha',0.1); npatches = numel(h);

% set fermi surface patch color
clist = flipud(parula(npatches)); for i = 1:npatches; set(h(i),'facecolor',clist(i,:),'edgecolor',clist(i,:)); end

% plot brillouin zone boundary
draw_fcc_bz_boundary()

h = line(-[0,0.6],-[0,0.6],[0,0.6]); set(h,'linewidth',2);
h = line(-[0,0.6],[0,0.6],-[0,0.6]); set(h,'linewidth',2);
h = line([0,0.6],-[0,0.6],-[0,0.6]); set(h,'linewidth',2);

